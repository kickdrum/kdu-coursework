package org.handson;

public class Student
{
int id;
String name;
int age;
char grade;
public void setID(int ID)
{
    id=ID;
}
public void setName(String Name)
{
    name=Name;
}
public void setAge(int Age)
{
    age=Age;
}
public void setGrade(char Grade)
{
    grade = Grade;
}
public int getID()
{
    return id;
}
public String getName()
{
    return name;
}
    public int getAge()
    {
        return age;
    }

    public char getGrade()
    {
        return grade;
    }
}

